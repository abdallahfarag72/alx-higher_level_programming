#!/usr/bin/python3

""" Defines a Square class """


class Square:
    """
    This is a class that represents a square.

    Attributes:
        __size (num): The size of the square.
    """

    def __init__(self, size):
        """
        Initializes a new instance of the Square class.

        Args:
            size (int): The size of the square.
        """
        self.__size = size
